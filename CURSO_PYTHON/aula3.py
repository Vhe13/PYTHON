"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
String são textos que estão dentro de aspas
"""

print(1234)

# Aspas Simples
print('Luiz Otávio')

# Aspas Duplas 
print("Luiz Otávio")

# Escape
print("Luiz\" Otávio\" ")

# r
print(r"Luiz \"Otávio\"")

# Aspas duplas dentro de aspas simples

print(1, 'Luiz "Otavio"')

# Aspas simples dentro de aspas duplas

print(2,"Luiz 'Otavio' ")